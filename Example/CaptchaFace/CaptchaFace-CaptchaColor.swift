import Foundation

extension UIColor
{
    class func captchaGray() -> UIColor
    {
        return UIColor(red: 147/255.0, green: 149/255.0, blue: 152/255.0, alpha: 1)
    }
    
    class func captchaBlue() -> UIColor
    {
        return UIColor(red: 0, green: 91/255.0, blue:126/255.0, alpha: 1)
    }
}